function toEnumName(name) {
  return "SPRITE_" + name.toUpperCase().replace(/-/g, "_");
}

function toDataName(name) {
  return "SPRITE_" + name.toUpperCase().replace(/-/g, "_") + "_DATA";
}

/** Use ImageMagick to resize + flatten PNG → raw RGB bytes, then convert to RGB565 */
function convertPngToRgb565(pngPath) {
  const tmpRaw = pngPath + ".raw";
  try {
    execSync(
      `magick "${pngPath}" ` +
        `-resize ${SPRITE_W}x${SPRITE_H}\\! ` +
        `-background white -flatten ` +
        `-depth 8 RGB:"${tmpRaw}"`,
      { stdio: "pipe" }
    );
    const raw = fs.readFileSync(tmpRaw);
    fs.unlinkSync(tmpRaw);

    const expected = SPRITE_W * SPRITE_H * 3;
    if (raw.length !== expected) {
      console.error(
        `    Size mismatch: got ${raw.length} bytes, expected ${expected}`
      );
      return null;
    }

    const pixels = new Uint16Array(SPRITE_W * SPRITE_H);
    for (let i = 0, p = 0; i < raw.length; i += 3, p++) {
      const r = raw[i],
        g = raw[i + 1],
        b = raw[i + 2];
      pixels[p] = ((r >> 3) << 11) | ((g >> 2) << 5) | (b >> 3);
    }
    return pixels;
  } catch (e) {
    console.error(`    Convert failed: ${e.message}`);
    if (fs.existsSync(tmpRaw)) fs.unlinkSync(tmpRaw);
    return null;
  }
}

function formatCArray(arrayName, pixels) {
  const lines = [];
  for (let i = 0; i < pixels.length; i += 16) {
    const end = Math.min(i + 16, pixels.length);
    const chunk = [];
    for (let j = i; j < end; j++) {
      chunk.push("0x" + pixels[j].toString(16).padStart(4, "0"));
    }
    lines.push("  " + chunk.join(","));
  }
  return `static const uint16_t ${arrayName}[] PROGMEM = {\n${lines.join(",\n")}\n};`;
}

function main() {
  console.log(`Input:  ${INPUT_DIR}`);
  console.log(`Output: ${OUTPUT_FILE}`);
  console.log(`Size:   ${SPRITE_W}x${SPRITE_H} (RGB565)\n`);

  const out = [];
  out.push("// Auto-generated by convert-sprites-to-header.mjs — do not edit");
  out.push("#pragma once");
  out.push("#include <Arduino.h>");
  out.push("");
  out.push(`#define SPRITE_W ${SPRITE_W}`);
  out.push(`#define SPRITE_H ${SPRITE_H}`);
  out.push("");

  const validEnums = [];
  const validDataNames = [];
  let success = 0,
    fail = 0;

  for (const name of SPRITE_NAMES) {
    const pngPath = path.join(INPUT_DIR, `${name}.png`);
    const enumName = toEnumName(name);
    const dataName = toDataName(name);

    if (!fs.existsSync(pngPath)) {
      console.log(`  SKIP: ${name}.png (not found)`);
      fail++;
      continue;
    }

    process.stdout.write(`  ${name}.png → ${dataName}...`);
    const pixels = convertPngToRgb565(pngPath);
    if (!pixels) {
      console.log(" FAIL");
      fail++;
      continue;
    }

    out.push(formatCArray(dataName, pixels));
    out.push("");
    validEnums.push(enumName);
    validDataNames.push(dataName);
    console.log(
      ` OK (${(pixels.length * 2 / 1024).toFixed(1)} KB)`
    );
    success++;
  }

  // Enum
  out.push("typedef uint8_t SpriteIdx;");
  out.push("enum {");
  for (const e of validEnums) {
    out.push(`  ${e},`);
  }
  out.push("  SPRITE_COUNT");
  out.push("};");
  out.push("");

  // Pointer table
  out.push("static const uint16_t* const SPRITE_DATA[] PROGMEM = {");
  for (const d of validDataNames) {
    out.push(`  ${d},`);
  }
  out.push("};");
  out.push("");

  fs.writeFileSync(OUTPUT_FILE, out.join("\n") + "\n");
  const sizeKB = (fs.statSync(OUTPUT_FILE).size / 1024).toFixed(0);

  console.log(`\n=== Done ===`);
  console.log(`Success: ${success} | Failed: ${fail}`);
  console.log(`Output: ${OUTPUT_FILE} (${sizeKB} KB)`);
  console.log(
    `Flash usage: ~${((success * SPRITE_W * SPRITE_H * 2) / 1024).toFixed(0)} KB for ${success} sprites`
  );
}

main();
